# This program checks if two strings are equal

# Ask the user to enter two strings
string1 = input("Enter the first string:")
string2 = input("Enter the second string:")

#Compare the string using == and !=

if string1 == string2:
    print("The string is equal")
else:
    print("The string is not equal")
# Additional check using !=
if string1 != string2:
    print("The string is not equal")
else:
    print("The string is equal")
